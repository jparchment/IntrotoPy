import turtle
turtle.bgcolor('green')

x = float(input('Enter any number: '))
if x == 100:
  turtle.color('purple')
  turtle.pensize(10)
  turtle.forward(200)
  
if x <= 0:
  turtle.dot(150,'orange')
  
if x > 0:
  turtle.dot(100, 'cyan')
  

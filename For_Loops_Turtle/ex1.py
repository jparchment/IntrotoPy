# draws a single square using turtle graphics

# imports turtle module
import turtle

# sets up turtle
turtle.showturtle()
turtle.shape('turtle')
turtle.color('purple')
turtle.start_fill()

# draws square
turtle.forward(100)
turtle.right(90)

turtle.forward(100)
turtle.right(90)

turtle.forward(100)
turtle.right(90)

turtle.forward(100)
turtle.right(90)
